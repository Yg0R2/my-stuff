public class Student {

	private String _name;
	private String _rollNo;

	public String getName() {
		return _name;
	}

	public String getRollNo() {
		return _rollNo;
	}

	public void setName(String name) {
		_name = name;
	}

	public void setRollNo(String rollNo) {
		_rollNo = rollNo;
	}

}